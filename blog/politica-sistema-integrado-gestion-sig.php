      <h2>Qué es la Política del Sistema Integrado de Gestión</h2>
      <p>Una política SIG no es un texto decorativo para auditorías. Es la declaración de dirección que alinea tres ejes críticos en una sola lógica de gestión:</p>
      <ul>
        <li><strong>Calidad:</strong> prestar servicios consistentes que aseguren satisfacción y fidelidad del cliente.</li>
        <li><strong>Medio ambiente:</strong> minimizar el impacto de las operaciones y promover el uso sostenible de recursos.</li>
        <li><strong>Seguridad y salud en el trabajo:</strong> prevenir lesiones, enfermedades e incidentes laborales con participación activa de los colaboradores.</li>
      </ul>
      <p>Al integrar estos frentes, la organización evita gestionar calidad, SSOMA y ambiente como silos aislados. El resultado es una operación más coherente, auditable y predecible.</p>

      <blockquote>
        <p>La política define el “para qué” y el “con qué criterio” se toman las decisiones operativas día a día.</p>
      </blockquote>

      <h2>Qué establece el documento PO-ALD-001</h2>
      <p>La política vigente (Lima, 2 de enero de 2026) reconoce a Cleaned como empresa de servicios integrales de limpieza y desinfección, mantenimiento, saneamiento ambiental, servicios aeroportuarios, fumigación y facility management. Sobre esa base, fija siete compromisos fundamentales:</p>
      <ol>
        <li>Garantizar la calidad del servicio y la satisfacción del cliente.</li>
        <li>Proteger el medio ambiente y preservar la calidad ambiental.</li>
        <li>Fomentar la participación activa de colaboradores y sus representantes en SST.</li>
        <li>Asegurar un entorno seguro y saludable, con medidas preventivas.</li>
        <li>Gestionar proactivamente los riesgos en todos los niveles de la organización.</li>
        <li>Cumplir leyes, normativas y requisitos aplicables en calidad, ambiente y SST.</li>
        <li>Impulsar la mejora continua del desempeño en calidad, sostenibilidad y seguridad laboral.</li>
      </ol>
      <p>Estos puntos no son aspiraciones genéricas: son criterios de decisión para procedimientos, capacitación, supervisión en campo y acciones correctivas.</p>

      <h2>Beneficios generales de contar con una política SIG</h2>
      <h3>Claridad de dirección</h3>
      <p>Una política escrita y comunicada reduce la ambigüedad. Equipos, supervisores y proveedores entienden qué prioriza la empresa cuando hay que elegir entre velocidad, costo y cumplimiento.</p>

      <h3>Marco para el Sistema de Gestión Integrado</h3>
      <p>La política es el punto de partida del SIG. Sin ella, los procedimientos, indicadores y auditorías pierden ancla. Con ella, ISO 9001, ISO 14001 e ISO 45001 se conectan a un mismo propósito organizacional.</p>

      <h3>Prevención frente a reacción</h3>
      <p>Al comprometerse con la identificación, evaluación y control de riesgos, la organización se orienta a prevenir incidentes, no solo a responder cuando ya ocurrieron. Eso protege personas, continuidad del servicio y reputación.</p>

      <h3>Cumplimiento y trazabilidad</h3>
      <p>Declarar el cumplimiento normativo como compromiso facilita alinear controles documentales, evidencias y auditorías internas. Para clientes exigentes, eso se traduce en menos fricción en homologaciones y revisiones de proveedor.</p>

      <h3>Mejora continua medible</h3>
      <p>La mejora continua deja de ser un eslogan cuando la política obliga a optimizar de forma constante el desempeño en calidad, sostenibilidad y seguridad. Eso abre la puerta a KPIs, planes de acción y ciclos PHVA reales.</p>

      <h2>Cómo le sirve esto a las empresas clientes</h2>
      <p>Para una empresa que contrata limpieza corporativa, facility management o servicios especializados, la política SIG del proveedor no es un detalle interno: es una señal de madurez operativa.</p>
      <ul>
        <li><strong>Menor riesgo transferido:</strong> un proveedor con política SIG activa reduce la exposición del cliente a incidentes laborales, incumplimientos ambientales o fallas de calidad en sus instalaciones.</li>
        <li><strong>Mayor consistencia del servicio:</strong> los compromisos de calidad y mejora continua se traducen en estándares repetibles entre turnos, sedes y equipos.</li>
        <li><strong>Facilidad en auditorías y compliance:</strong> cuando el cliente debe demostrar control de proveedores, un documento público y vigente como PO-ALD-001 aporta evidencia clara de compromiso formal.</li>
        <li><strong>Alineación con estándares corporativos:</strong> muchas organizaciones exigen a sus contratistas políticas de SST, ambiente y calidad. Contar con una política integrada simplifica esa evaluación.</li>
        <li><strong>Confianza comercial:</strong> en entornos B2B, la transparencia documental refuerza la relación: el cliente sabe qué espera del proveedor y bajo qué criterios se gestiona el servicio.</li>
      </ul>

      <h2>De la política a la operación</h2>
      <p>El valor de la Política SIG se concreta cuando se despliega en campo: procedimientos, formación, uso correcto de EPP, control de químicos, gestión de residuos, supervisión y cierre de hallazgos. Por eso en Cleaned la política no se limita al archivo: se conecta con certificaciones ISO, controles SSOMA y la disciplina operativa diaria.</p>
      <p>Si tu organización evalúa proveedores o busca un partner de servicios con gestión integrada, te invitamos a revisar el documento y a conversar sobre cómo aplicamos estos compromisos en tu operación.</p>

