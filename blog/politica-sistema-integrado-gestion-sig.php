      <p>En servicios de limpieza, facility management y operaciones especializadas, la calidad no se improvisa: se declara, se documenta y se ejecuta. La <strong>Política Integrada de Sistemas de Gestión en Seguridad y Salud en el Trabajo, Medio Ambiente y Calidad</strong> (código <strong>PO-ALD-001</strong>, versión 02) es el documento que formaliza ese compromiso en Cleaned Perfect S.A.C. y orienta la conducta de la organización frente a clientes, colaboradores, contratistas y la comunidad.</p>

      <p>Puedes consultar el documento completo aquí:</p>
